# iteration

# iteration - for
print('demo - iteration for')
for i in range(1, 5):
    print(i)


# nested - for
print('demo - nested for')
for i in range(1, 3):
    for j in range(5, 10):
        print(str(i) + '-' + str(j))


# iteration - while
print('demo - iteration while')
i = 0
while i < 10:
    print(i)
    i += 1


